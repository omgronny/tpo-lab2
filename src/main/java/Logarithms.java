

public class Logarithms {

    static double ln(double x) {
        return 0.0;
    }

    static double log(double base, double x) {
        return ln(x) / ln(base);
    }

    static double log_2(double x) {
        return log(2, x);
    }

    static double log_5(double x) {
        return log(5, x);
    }

    static double log_3(double x) {
        return log(5, x);
    }

    static double log_10(double x) {
        return log(10, x);
    }

}

